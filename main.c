// Select internal FRC at POR
_FOSCSEL(FNOSC_FRC);
// Enable clock switching and configure
_FOSC(FCKSM_CSECMD & OSCIOFNC_OFF);

_FWDT(FWDTEN_OFF); 			            			// Watchdog Timer Enabled/disabled by user software

void Init_Timer1( void )
{
    // Timer1 - divides F_clk by 1,000,000 (so F_cy by 500,000)
    //          for F_clk 80MHz -> Interrupt Freq is 80Hz
    // copied from code generated by MCC for dsPIC33EP64MC203
    //TMR1 0; 
    TMR1 = 0x00;
    //Period = 0.125 s; Frequency = 4000000 Hz; PR1 62499; 
    PR1 = 0xF423;
    //TCKPS 1:8; TON enabled; TSIDL disabled; TCS FOSC/2; TSYNC disabled; TGATE disabled; 
    T1CON = 0x8010;
    IFS0bits.T1IF = 0;
    IEC0bits.T1IE = 1;
}

void Init_Timer2( void )
{
    // divide f_clock by 1000 (f_cy by 500)
    // copied from code generated by MCC for dsPIC33EP64MC203

    //TMR2 0; 
    TMR2 = 0x00;
    //Period = 0.000125 s; Frequency = 4000000 Hz; PR2 499; 
    PR2 = 0x1F3;
    //TCKPS 1:1; T32 16 Bit; TON enabled; TSIDL disabled; TCS FOSC/2; TGATE disabled; 
    T2CON = 0x8000;
    IFS0bits.T2IF = 0;
    IEC0bits.T2IE = 1;
}

void __attribute__((interrupt,no_auto_psv)) _T2Interrupt( void )
{
    // Interrupt freq is f_clk / 1000
    // output f_cy / 1000
	LATAbits.LATA1 = ~LATAbits.LATA1;		    
    IFS0bits.T2IF = 0;
}


// NOTE: volatile is absolutely necessary for XC16 v1.70
volatile unsigned int Count;

/* ISR ROUTINE FOR THE TIMER1 INTERRUPT */

void __attribute__((interrupt,no_auto_psv)) _T1Interrupt( void )
{
    Count++; // currently unused
    // RA0 blinks with f_clk / 1,000,000 / 10 / 2
    if (Count == 10){
        Count = 0;
        LATAbits.LATA0 = ~LATAbits.LATA0;
    }
    // must clear interrupt to confirm
    IFS0bits.T1IF = 0;	
}
