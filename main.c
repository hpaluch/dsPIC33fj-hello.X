int main(void)
{
	
	RCONbits.SWDTEN=0;            /* Disable Watch Dog Timer*/
    
    // attempt to set FRC to expected 7.37MHz
    // my FRC seems to be running on 7.3348 MHz
    OSCTUN = 0x0001;  // got f_cy = 3.6922 MHz => f_frc = 7.3678 MHz
    // NOTE: Your FRC may require different values of OSCTUN!!!

#define USE_FRC_PLL    
#ifdef USE_FRC_PLL    
    // Now switch from FRC f = 7.37Mhz to 80MHz (40MIPS) using PLL
    // From Example 48-2
    //      of http://ww1.microchip.com/downloads/en/DeviceDoc/DS-70596A.pdf
    // Configure PLL prescaler, PLL postscaler, and PLL divisor
    PLLFBD = 41; // M = 43
    CLKDIVbits.PLLPRE=0; // N1 = 2
    CLKDIVbits.PLLPOST=0; // N2 = 2
    // Initiate clock switch to internal FRC with PLL (NOSC = 0b001)
    __builtin_write_OSCCONH(0x01);
    __builtin_write_OSCCONL(0x01);
    // Wait for clock switch to occur
    while (OSCCONbits.COSC != 0b001){/*nop*/};
    // Wait for PLL to lock
    while(OSCCONbits.LOCK!=1){/*nop*/};
    // now we should be running on f = 80MHz, f_cy = 40 MHz (40MIPS)
#endif
    
   /*Initialize the Ports */    
    TRISA = 0x0000;
    LATA  = 0x0000;
    PORTA = 0x0000;

    /*Initialize Timer 1 for Period Interrupts*/

    Init_Timer1();

    Init_Timer2();
	
	while(1)
	{
        asm("NOP");
        asm("NOP");
	}
}
